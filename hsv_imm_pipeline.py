def area_xyxy(x1, y1, x2, y2) -> float:
    return max(0.0, x2 - x1) * max(0.0, y2 - y1)


def center_xyxy(x1, y1, x2, y2):
    return (0.5 * (x1 + x2), 0.5 * (y1 + y2))


def dist(a, b):
    return math.hypot(a[0] - b[0], a[1] - b[1])


def _clip_int(v, lo, hi):
    return int(max(lo, min(hi, v)))


def _roi_from_center(cx, cy, half, w, h):
    x1 = _clip_int(cx - half, 0, w - 1)
    y1 = _clip_int(cy - half, 0, h - 1)
    x2 = _clip_int(cx + half, 0, w - 1)
    y2 = _clip_int(cy + half, 0, h - 1)
    if x2 <= x1: x2 = min(w - 1, x1 + 1)
    if y2 <= y1: y2 = min(h - 1, y1 + 1)
    return x1, y1, x2, y2


def _learn_hsv_model(frame_bgr, xyxy):
    """
    Build a robust HSV range from pixels inside the chosen YOLO/track box.
    Handles red hue wrap-around by returning 1 or 2 hue ranges.
    """
    h, w = frame_bgr.shape[:2]
    x1, y1, x2, y2 = map(int, xyxy)
    x1 = _clip_int(x1, 0, w - 1); x2 = _clip_int(x2, 0, w - 1)
    y1 = _clip_int(y1, 0, h - 1); y2 = _clip_int(y2, 0, h - 1)
    if x2 <= x1 or y2 <= y1:
        return None

    crop = frame_bgr[y1:y2, x1:x2]
    if crop.size == 0:
        return None

    hsv = cv2.cvtColor(crop, cv2.COLOR_BGR2HSV)
    H = hsv[:, :, 0].astype(np.int32)
    S = hsv[:, :, 1].astype(np.int32)
    V = hsv[:, :, 2].astype(np.int32)

    good = (S >= HSV_MIN_S) & (V >= HSV_MIN_V)
    if good.sum() < 20:
        good = np.ones_like(H, dtype=bool)

    h_med = int(np.median(H[good]))
    s_med = int(np.median(S[good]))
    v_med = int(np.median(V[good]))

    s_lo = _clip_int(s_med - HSV_S_MARGIN, 0, 255)
    s_hi = _clip_int(s_med + HSV_S_MARGIN, 0, 255)
    v_lo = _clip_int(v_med - HSV_V_MARGIN, 0, 255)
    v_hi = _clip_int(v_med + HSV_V_MARGIN, 0, 255)

    h_lo = (h_med - HSV_H_MARGIN) % 180
    h_hi = (h_med + HSV_H_MARGIN) % 180

    if h_lo <= h_hi:
        hue_ranges = [((h_lo, s_lo, v_lo), (h_hi, s_hi, v_hi))]
    else:
        hue_ranges = [((0,    s_lo, v_lo), (h_hi, s_hi, v_hi)),
                      ((h_lo, s_lo, v_lo), (179,  s_hi, v_hi))]

    return {"hue_ranges": hue_ranges}


def _hsv_blob_in_roi(frame_bgr, roi_xyxy, hsv_model, pred_xy=None):
    """
    Threshold HSV in ROI, find best blob (closest to pred if given),
    return xyxy in FULL-FRAME coords.
    """
    if hsv_model is None:
        return None

    H, W = frame_bgr.shape[:2]
    rx1, ry1, rx2, ry2 = map(int, roi_xyxy)
    rx1 = _clip_int(rx1, 0, W - 1); rx2 = _clip_int(rx2, 0, W - 1)
    ry1 = _clip_int(ry1, 0, H - 1); ry2 = _clip_int(ry2, 0, H - 1)
    if rx2 <= rx1 or ry2 <= ry1:
        return None

    roi = frame_bgr[ry1:ry2, rx1:rx2]
    if roi.size == 0:
        return None

    hsv = cv2.cvtColor(roi, cv2.COLOR_BGR2HSV)

    mask = None
    for (lo, hi) in hsv_model["hue_ranges"]:
        lo = np.array(lo, dtype=np.uint8)
        hi = np.array(hi, dtype=np.uint8)
        m = cv2.inRange(hsv, lo, hi)
        mask = m if mask is None else cv2.bitwise_or(mask, m)

    s = hsv[:, :, 1]
    v = hsv[:, :, 2]
    mask = cv2.bitwise_and(mask, cv2.inRange(s, HSV_MIN_S, 255))
    mask = cv2.bitwise_and(mask, cv2.inRange(v, HSV_MIN_V, 255))

    k = HSV_MORPH_KERNEL
    if k >= 3:
        kernel = np.ones((k, k), np.uint8)
        mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN,  kernel, iterations=HSV_MORPH_ITERS)
        mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel, iterations=HSV_MORPH_ITERS)

    contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    best = None
    best_score = float("inf")

    for c in contours:
        area = cv2.contourArea(c)
        if area < HSV_MIN_AREA_PX or area > HSV_MAX_AREA_PX:
            continue

        x, y, w, h = cv2.boundingRect(c)
        if w <= 0 or h <= 0:
            continue

        aspect = w / float(h)
        if aspect < HSV_ASPECT_MIN or aspect > HSV_ASPECT_MAX:
            continue

        cx = x + 0.5 * w
        cy = y + 0.5 * h

        if pred_xy is not None:
            px, py = pred_xy
            px -= rx1; py -= ry1
            score = math.hypot(cx - px, cy - py)
        else:
            score = area

        if score < best_score:
            best_score = score
            best = (x, y, x + w, y + h)

    if best is None:
        return None

    bx1, by1, bx2, by2 = best
    return (float(bx1 + rx1), float(by1 + ry1), float(bx2 + rx1), float(by2 + ry1))


