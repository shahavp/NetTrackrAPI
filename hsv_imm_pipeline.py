def main():
    out_dir = Path(OUT_DIR)
    out_dir.mkdir(parents=True, exist_ok=True)

    tracker_yaml = out_dir / "bytetrack_custom.yaml"
    write_bytetrack_yaml(tracker_yaml)

    # ── Step 1: detection & tracking ─────────────────────────────────────────
    print("=" * 60)
    print("  Detection & Tracking  (YOLO + ByteTrack + HSV)")
    print("=" * 60)

    model  = YOLO(MODEL_PATH)
    stream = model.track(
        source=VIDEO_PATH, imgsz=IMGSZ, conf=CONF, iou=IOU, device=DEVICE,
        classes=CLASSES, tracker=str(tracker_yaml), persist=True, stream=True,
        verbose=False, save=False,
    )

    writer       = None
    video_fps    = 30.0
    csv_rows     = []
    active_id    = None
    last_pos     = None
    vel          = np.array([0.0, 0.0], dtype=float)
    misses       = 0
    hsv_model    = None
    last_box_area = None

    for fi, r in enumerate(stream):
        frame = r.orig_img
        if frame is None:
            continue

        if writer is None:
            h, w = frame.shape[:2]
            cap_tmp = cv2.VideoCapture(VIDEO_PATH)
            if cap_tmp.isOpened():
                fps_val = cap_tmp.get(cv2.CAP_PROP_FPS)
                if fps_val and fps_val > 0:
                    video_fps = fps_val
            cap_tmp.release()
            fourcc = cv2.VideoWriter_fourcc(*"mp4v")
            writer = cv2.VideoWriter(
                str(out_dir / "annotated_output.mp4"), fourcc, video_fps, (w, h))

        candidates = []
        if r.boxes is not None and len(r.boxes) > 0:
            xyxy  = r.boxes.xyxy.cpu().numpy()
            confs = r.boxes.conf.cpu().numpy()
            clss  = r.boxes.cls.cpu().numpy().astype(int)
            ids   = None
            if getattr(r.boxes, "id", None) is not None:
                ids = r.boxes.id.cpu().numpy().astype(int)
            for i in range(len(xyxy)):
                x1, y1, x2, y2 = xyxy[i]
                if MIN_BOX_AREA_PX > 0 and area_xyxy(x1, y1, x2, y2) < MIN_BOX_AREA_PX:
                    continue
                candidates.append({
                    "xyxy": (float(x1), float(y1), float(x2), float(y2)),
                    "conf": float(confs[i]),
                    "cls":  int(clss[i]),
                    "id":   int(ids[i]) if ids is not None else None,
                })

        pred = None
        if last_pos is not None:
            pred = (last_pos[0] + float(vel[0]), last_pos[1] + float(vel[1]))

        chosen = None
        gate   = GATE_RADIUS_PX + misses * GATE_GROW_PER_MISS

        if active_id is None:
            if candidates:
                chosen    = max(candidates, key=lambda d: d["conf"])
                active_id = chosen["id"]
        else:
            same_id = [d for d in candidates if d["id"] == active_id]
            pool    = same_id if same_id else candidates
            if pool and pred is not None:
                best = min(pool, key=lambda d: dist(center_xyxy(*d["xyxy"]), pred))
                if dist(center_xyxy(*best["xyxy"]), pred) <= gate:
                    chosen = best
            elif pool:
                chosen = max(pool, key=lambda d: d["conf"])

        hsv_recovered = False
        if chosen is not None and ENABLE_HSV_RECOVERY:
            hsv_model     = _learn_hsv_model(frame, chosen["xyxy"])
            x1, y1, x2, y2 = chosen["xyxy"]
            last_box_area = area_xyxy(x1, y1, x2, y2)

        if chosen is None and ENABLE_HSV_RECOVERY and hsv_model is not None and pred is not None:
            Hh, Ww = frame.shape[:2]
            half   = int(gate + HSV_ROI_PAD_PX)
            rx1, ry1, rx2, ry2 = _roi_from_center(pred[0], pred[1], half, Ww, Hh)
            bb = _hsv_blob_in_roi(frame, (rx1, ry1, rx2, ry2), hsv_model, pred_xy=pred)
            if bb is not None:
                chosen        = {"xyxy": bb, "conf": 0.0, "cls": 0, "id": active_id}
                hsv_recovered = True

        status = "miss"
        if chosen is not None:
            cx, cy = center_xyxy(*chosen["xyxy"])
            if last_pos is not None:
                new_vel = np.array([cx - last_pos[0], cy - last_pos[1]], dtype=float)
                vel     = 0.7 * vel + 0.3 * new_vel
            last_pos = (cx, cy)
            misses   = 0
            status   = "hsv" if hsv_recovered else "det"
            if chosen["id"] is not None:
                active_id = chosen["id"]
        else:
            misses += 1
            status  = "pred" if pred is not None else "miss"
            if misses > MAX_MISSES:
                active_id     = None
                last_pos      = None
                vel[:]        = 0.0
                hsv_model     = None
                last_box_area = None
                misses        = 0
                status        = "reset"

        vis = frame.copy()
        if chosen is not None:
            x1, y1, x2, y2 = map(int, chosen["xyxy"])
            cv2.rectangle(vis, (x1, y1), (x2, y2), (0, 255, 0), 2)
        if writer is not None:
            writer.write(vis)

        csv_rows.append({
            "frame":  fi,
            "status": status,
            "id":     active_id if active_id is not None else "",
            "x":      last_pos[0] if last_pos is not None else "",
            "y":      last_pos[1] if last_pos is not None else "",
            "vx":     float(vel[0]),
            "vy":     float(vel[1]),
            "gate":   gate,
            "misses": misses,
        })

        if fi % 100 == 0:
            print(f"  frame {fi} …", end='\r')

    if writer is not None:
        writer.release()

    csv_path = out_dir / "trajectory.csv"
    with csv_path.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=list(csv_rows[0].keys()))
        w.writeheader(); w.writerows(csv_rows)
    print(f"\n  Tracking done. CSV: {csv_path}")

    # ── Step 2: IMM + UKF release speed ──────────────────────────────────────
    print("\n" + "=" * 60)
    print("  IMM + UKF Release Speed Estimation")
    print("=" * 60)
    imm_res = run_imm_speed(csv_rows, video_fps)

    release_kmh = imm_res.get("release_speed_kmh", 0.0) if imm_res.get("imm_success") else 0.0

    # ── Step 3: trajectory smoothing ─────────────────────────────────────────
    print("\n" + "=" * 60)
    print("  Trajectory Smoothing  (parabolic)")
    print("=" * 60)
    try:
        traj = load_trajectory_from_rows(csv_rows)
        print(f"  {len(traj['points'])} pts, "
              f"frames {traj['start_frame']}--{traj['end_frame']}")
        if traj['bounce']:
            print(f"  Bounce at ({traj['bounce'][0]:.1f}, {traj['bounce'][1]:.1f})")
    except ValueError as e:
        print(f"  Trajectory failed: {e}")
        return

    # ── Step 4: render final video ───────────────────────────────────────────
    print("\n" + "=" * 60)
    print("  Rendering overlay video")
    print("=" * 60)

    ann_path = str(out_dir / "annotated_output.mp4")
    cap = cv2.VideoCapture(ann_path)
    if not cap.isOpened():
        raise IOError(f"Can't open annotated video: {ann_path}")

    fps_v = cap.get(cv2.CAP_PROP_FPS)
    w     = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    h     = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    print(f"  {w}x{h}, {fps_v:.1f}fps, {total} frames")

    out = cv2.VideoWriter(OUTPUT_VIDEO, cv2.VideoWriter_fourcc(*'mp4v'), fps_v, (w, h))

    fn = 0
    while True:
        ret, frame = cap.read()
        if not ret:
            break
        if fn >= traj['start_frame']:
            draw_overlay(frame, traj, LINE_COLOR, release_kmh)
        out.write(frame)
        fn += 1
        if fn % 50 == 0:
            print(f"  {100 * fn / max(total, 1):.0f}%", end='\r')

    cap.release()
    out.release()

    print(f"\n" + "=" * 60)
    print("  DONE")
    print("=" * 60)
    print(f"  Output video : {OUTPUT_VIDEO}")
    print(f"  Trajectory   : {csv_path}")
    if release_kmh:
        print(f"  Release speed: {release_kmh:.1f} km/h")


if __name__ == "__main__":
    main()